      double precision function fxn(xx,wgt)
      implicit double precision (a-z)
      common/cnsts/pi,zeta2
      dimension xx(16)
c Region 1: 0<p+q<1
c Limits are 0 to 1 for xx(1), xx(2)
c and -1 to 1 for ut=xx(3)
      xp = xx(1)
      xq = xx(2)
      x = xx(3)
      u = xx(4)
      thq = xx(5)
      z = xx(6)
      p = xp*(1.d0-xq)
      q = xp*xq
      a = z*(u+x-2.*u*x-1.+x*u*u)
c
c
      fxx = (fxns(p,q,x,u,   thq,z)
     1     + fxns(p,q,x,u,pi-thq,z))/2.
c
c
      fxn = -2.d0*(2./3.)*(1/a)*fxx*xp
      return
      end
